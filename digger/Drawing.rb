  def makefield
    x = 0
    while x < 15
      y = 0
      while y < 10
        @field[y * 15 + x] = -1
        c = @dig.Main.getlevch(x, y, @dig.Main.levplan())
        if c == "S" || c == "V"
          @field[y * 15 + x] &= 0xd03f
        end

        if c == "S" || c == "H"
          @field[y * 15 + x] &= 0xdfe0
        end

        if @dig.Main.getcplayer() == 0
          @field1[y * 15 + x] = @field[y * 15 + x]
        else
          @field2[y * 15 + x] = @field[y * 15 + x]
        end

        y += 1
      end

      x += 1
    end
  end

  def outtext(p, x, y, c, b = false)
    rx = x

    i = 0
    while i < p.length()
      @dig.Pc.gwrite(x, y, p[i], c)
      x += 12

      i += 1
    end

    if b
      @dig.Pc.currentSource.new_pixels(rx, y, p.length() * 12, 12)
    end
  end

  def savefield
    x = 0
    while x < 15
      y = 0
      while y < 10
        if @dig.Main.getcplayer() == 0
          @field1[y * 15 + x] = @field[y * 15 + x]
        else
          @field2[y * 15 + x] = @field[y * 15 + x]
        end

        y += 1
      end

      x += 1
    end
  end
